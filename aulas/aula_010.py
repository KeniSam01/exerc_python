tempo = int(input("Quantos anos tem seu carro? ")) #interação com o usuario

if tempo <= 3: # argumento de condição se for verdadeiro
    print("Seu carro é novo!")
    print("---FIM---")
else: # argumento de condição se for falso
    print("Seu carro está muito velho!")
    print("---FIM---")

